// 설정

function setup () {

   createCanvas(600,400);
}

// 디스플레이

function draw () {

  background(50);

  lollipop();
}

// -----------------------------------------------------------------
// 사용자 정의 함수들
// -----------------------------------------------------------------

// 막대 사탕 드로잉

function lollipop () {

  // 막대
  fill(0,200,255);
  rect(290,200,20,150);

  // 사탕
  fill(255,0,200);
  ellipse(300,200,100,100);
}